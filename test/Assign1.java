class Assign1 {
    public static void main(String[] args) {
        int x = 3;
        System.out.println(x);

        x = 1;
        System.out.println(x);

        x += 2;
        System.out.println(x);

        x -= 10;
        System.out.println(x);

        x *= 2;
        System.out.println(x);

        x /= 14;
        System.out.println(x);

        x = 6;
        x &= 2;
        System.out.println(x);

        x = 10;
        x |= 3;
        System.out.println(x);

        x = 10;
        x ^= 3;
        System.out.println(x);

        x = 10;
        x %= 3;
        System.out.println(x);

        x = 3;
        x <<= 1;
        System.out.println(x);

        x = 10;
        x >>= 1;
        System.out.println(x);

    }
}
